package OOP;

public abstract class AbstractClass {
	
	//Abstraction is concept of OOP hiding an implementation 
	//Abstract does't have any implementation
	//Abstract class have only 
	
	public abstract void iDoor(); //always starts with i...
	
	public abstract String iEngine();
	
	public abstract String iColor();
	
	
	public abstract boolean iHybrid();
	
	
	public abstract int iSeats();
	
	
	public void car() {
		System.out.println("this car is Kia");
	}
	

}
